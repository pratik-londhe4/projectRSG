#!/bin/bash

spinner() {
    local i sp n
    sp='/-\|'
    n=${#sp}
    printf ' '
    while sleep 0.1; do
        printf "%s\b" "${sp:i++%n:1}"
    done
}







echo ''
echo "                    =========================================================";
echo "                                 ########   ######   ######                   ";   
echo "                                 ##     ## ##    ## ##    ##                  ";  
echo "                                 ##     ## ##       ##                        ";        
echo "                                 ########   ######  ##   ####                 "; 
echo "                                 ##   ##         ## ##    ##                  ";  
echo "                                 ##    ##  ##    ## ##    ##                  ";  
echo "                                 ##     ##  ######   ######                   ";   
echo "                    =========================================================   ";

printf '                                Loading the best song for you-'

spinner&
sleep 2


printf '\n'
echo "                    ===========================================================";
python RSG.py
echo "                    ===========================================================";

kill "$!"











